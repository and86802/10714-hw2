class ReLU(TensorOp):
    def compute(self, a):
        ### BEGIN YOUR SOLUTION
        return array_api.maximum(0, a)
        ### END YOUR SOLUTION

    def gradient(self, out_grad, node):
        ### BEGIN YOUR SOLUTION
        data = node.inputs[0].realize_cached_data()
        relu_data = array_api.zeros_like(data)
        relu_data[data > 0] = 1
        return multiply(out_grad, Tensor(relu_data))
        ### END YOUR SOLUTION


def relu(a):
    return ReLU()(a)

